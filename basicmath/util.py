def add_num(first_no, second_no):
    ''' Takes two numbers as parameters and returns their sum '''
    return first_no + second_no

def sub_num(first_no, second_no):
    ''' Takes two numbers as parameters and returns their differe   nce '''
    return first_no - second_no

def mul_num(first_no, second_no):
    ''' Takes two numbers as parameters and returns their product '''
    return first_no * second_no

def div_num(first_no, second_no):
    ''' Takes two numbers as parameters and returns a/b '''
    return first_no / second_no
